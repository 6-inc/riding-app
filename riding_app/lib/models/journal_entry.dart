class JournalEntry {
  final String title;
  final String content;
  final String style;
  final String horse;
  final String location;
  final DateTime startTime;
  final DateTime endTime;

  JournalEntry({
    required this.title,
    required this.content,
    required this.style,
    required this.horse,
    required this.location,
    required this.startTime,
    required this.endTime,
  });
}
